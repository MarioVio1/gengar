// Funzione per ottenere film casuale
export function getRandomMovies(count: number = 10): Movie[] {
  const shuffled = [...randomMoviePool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

// Funzione per filtrare per genere
export function filterByGenre(movies: Movie[], genre: string): Movie[] {
  if (!genre || genre === "all") return movies;
  return movies.filter(m => m.genres?.some(g => g.toLowerCase().includes(genre.toLowerCase())));
}

// Funzione per filtrare per anno
export function filterByYear(movies: Movie[], yearRange: string): Movie[] {
  if (!yearRange || yearRange === "all") return movies;
  const [start, end] = yearRange.split("-").map(Number);
  return movies.filter(m => m.year >= start && m.year <= (end || start + 9));
}

